            // Add glitch effect on hover
            const serviceCards = document.querySelectorAll('.service-card');
            
            serviceCards.forEach(card => {
                card.addEventListener('mouseenter', () => {
                    // Add random glitch effect
                    const glitchEffect = () => {
                        const randomX = Math.random() * 10 - 5;
                        const randomY = Math.random() * 10 - 5;
                        const randomSkewX = Math.random() * 10 - 5;
                        const randomSkewY = Math.random() * 10 - 5;
                        
                        card.style.transform = `translateY(-10px) translate(${randomX}px, ${randomY}px) skew(${randomSkewX}deg, ${randomSkewY}deg)`;
                        
                        setTimeout(() => {
                            card.style.transform = 'translateY(-10px)';
                        }, 100);
                    };
                    
                    // Apply glitch effect a few times
                    setTimeout(glitchEffect, 300);
                    setTimeout(glitchEffect, 700);
                    setTimeout(glitchEffect, 1200);
                });
            });
            
            // Parallax effect on mountains and sun
            window.addEventListener('mousemove', (e) => {
                const mouseX = e.clientX / window.innerWidth - 0.5;
                const mouseY = e.clientY / window.innerHeight - 0.5;
                
                const sun = document.querySelector('.sun');
                const mountains = document.querySelectorAll('.mountain');
                
                sun.style.transform = `translate(${mouseX * 20}px, ${mouseY * 20}px)`;
                
                mountains.forEach((mountain, index) => {
                    const depth = (index + 1) * 5;
                    mountain.style.transform = `translateX(${mouseX * depth}px)`;
                });
            });
            
            // Random glitch effect on title
            setInterval(() => {
                const glitchTexts = document.querySelectorAll('.glitch');
                
                glitchTexts.forEach(text => {
                    if (Math.random() > 0.9) {
                        text.style.textShadow = `
                            ${Math.random() * 10 - 5}px ${Math.random() * 10 - 5}px 0 rgba(255,0,255,0.7),
                            ${Math.random() * 10 - 5}px ${Math.random() * 10 - 5}px 0 rgba(0,255,255,0.7)
                        `;
                        
                        setTimeout(() => {
                            text.style.textShadow = '5px 5px 0px #ff6ad5';
                        }, 100);
                    }
                });
            }, 2000);
        </script>
