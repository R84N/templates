        <script>
            // Make the services grid draggable for horizontal scrolling
            const servicesGrid = document.getElementById('services-grid');
            let isDown = false;
            let startX;
            let scrollLeft;
            
            servicesGrid.addEventListener('mousedown', (e) => {
                isDown = true;
                servicesGrid.style.cursor = 'grabbing';
                startX = e.pageX - servicesGrid.offsetLeft;
                scrollLeft = servicesGrid.scrollLeft;
            });
            
            servicesGrid.addEventListener('mouseleave', () => {
                isDown = false;
                servicesGrid.style.cursor = 'grab';
            });
            
            servicesGrid.addEventListener('mouseup', () => {
                isDown = false;
                servicesGrid.style.cursor = 'grab';
            });
            
            servicesGrid.addEventListener('mousemove', (e) => {
                if (!isDown) return;
                e.preventDefault();
                const x = e.pageX - servicesGrid.offsetLeft;
                const walk = (x - startX) * 2; // Scroll speed
                servicesGrid.scrollLeft = scrollLeft - walk;
            });
            
            // Parallax effect on service cards
            const serviceCards = document.querySelectorAll('.service-card');
            
            window.addEventListener('mousemove', (e) => {
                const mouseX = e.clientX / window.innerWidth - 0.5;
                const mouseY = e.clientY / window.innerHeight - 0.5;
                
                serviceCards.forEach(card => {
                    const cardRect = card.getBoundingClientRect();
                    const cardCenterX = cardRect.left + cardRect.width / 2;
                    const cardCenterY = cardRect.top + cardRect.height / 2;
                    
                    const offsetX = (e.clientX - cardCenterX) * 0.01;
                    const offsetY = (e.clientY - cardCenterY) * 0.01;
                    
                    card.style.transform = `translate(${offsetX}px, ${offsetY}px)`;
                    
                    const bg = card.querySelector('.service-card-bg');
                    bg.style.transform = `translate(${-offsetX * 2}px, ${-offsetY * 2}px) scale(1.05)`;
                });
            });
            
            // Scroll animation
            window.addEventListener('scroll', () => {
                const scrollY = window.scrollY;
                
                // Parallax effect for background shapes
                document.querySelector('.services-shape-1').style.transform = `translateY(${scrollY * 0.1}px)`;
                document.querySelector('.services-shape-2').style.transform = `translateY(${scrollY * -0.05}px) rotate(${scrollY * 0.05}deg)`;
                document.querySelector('.services-shape-3').style.transform = `translateY(${scrollY * 0.07}px) rotate(${scrollY * -0.05}deg)`;
            });
        </script>