    <script>
        // Simple script to animate the timeline progress as user scrolls
        document.addEventListener('DOMContentLoaded', function() {
            const progressBar = document.querySelector('.timeline-progress');
            const timelineHeight = document.querySelector('.team-timeline').offsetHeight;
            
            function updateProgress() {
                const scrollPosition = window.scrollY;
                const sectionTop = document.querySelector('.gallery5').offsetTop;
                const scrollPercentage = Math.min(100, Math.max(0, (scrollPosition - sectionTop) / (timelineHeight - 300) * 100));
                progressBar.style.height = scrollPercentage + '%';
            }
            
            window.addEventListener('scroll', updateProgress);
            updateProgress(); // Initial call
        });
    </script>